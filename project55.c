#include <stdio.h>

int main() {
    double S, V1, V2, t;
    
    printf("Введите расстояния между автомобилями (км): ");
    scanf("%lf", &S);
    
    printf("Введите скорость первого автомобиля (км/ч): ");
    scanf("%lf", &V1);
    
    printf("Введите скорость второго автомобиля (км/ч): ");
    scanf("%lf", &V2);
    
    if (V1 <= 0 || V2 <= 0) {
        printf("Ошибка: скорости должны быть положительными и больше нуля!\n");
        return 1;
    }
    
    if (S <= 0) {
        printf("Ошибка: расстояние должно быть положительным и больше нуля!\n");
        return 1;
    }
    
    t = S / (V1 + V2);
    
    printf("Автомобили встретятся через %.2f часа.\n", t);
}
